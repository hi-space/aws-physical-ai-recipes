function generateRLCurve(numPoints: number, maxReward: number, noise: number): number[] {
  const curve: number[] = [];
  for (let i = 0; i < numPoints; i++) {
    const t = i / numPoints;
    const base = maxReward * (1 - Math.exp(-4 * t));
    const jitter = (Math.random() - 0.5) * noise * 2;
    curve.push(Math.max(0, base + jitter));
  }
  return curve;
}

function generateDecayingCurve(numPoints: number, startVal: number, endVal: number, noise: number): number[] {
  const curve: number[] = [];
  for (let i = 0; i < numPoints; i++) {
    const t = i / numPoints;
    const base = startVal * Math.exp(-3 * t) + endVal;
    const jitter = (Math.random() - 0.5) * noise * 2;
    curve.push(Math.max(0, base + jitter));
  }
  return curve;
}

